atom = [np.str_('C'), np.str_('O'), np.str_('C'), np.str_('C'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('O'), np.str_('H')]
geom = [[np.float64(1.245959), np.float64(0.938213), np.float64(-0.324184)], [np.float64(1.679784), np.float64(-0.476147), np.float64(0.701186)], [np.float64(-0.039308), np.float64(-0.078768), np.float64(-0.003662)], [np.float64(-0.538873), np.float64(0.242474), np.float64(1.397816)], [np.float64(-1.816327), np.float64(0.605263), np.float64(1.416266)], [np.float64(-0.458872), np.float64(-0.798816), np.float64(2.226159)], [np.float64(0.110964), np.float64(1.235541), np.float64(1.996492)], [np.float64(-0.840362), np.float64(0.398153), np.float64(-0.936273)], [np.float64(-0.030122), np.float64(-1.343537), np.float64(-0.339742)], [np.float64(2.47197), np.float64(1.39842), np.float64(-0.524286)], [np.float64(3.144681), np.float64(0.850616), np.float64(-0.097674)]]
