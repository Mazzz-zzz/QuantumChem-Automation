db = connect('/home/akhalilov/GaussianJobCreator/AUTOMATION-CENTER/kinbot_jobs/PFPra-M062X/kinbot.db')
label = '1642764945472630600001_r13_insertion_ROR_8_3_1_10'
logfile = '1642764945472630600001_r13_insertion_ROR_8_3_1_10.log'

atom = [np.str_('C'), np.str_('O'), np.str_('C'), np.str_('C'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('O'), np.str_('H')]
geom = [[np.float64(2.335904), np.float64(1.261497), np.float64(-0.124325)], [np.float64(3.204509), np.float64(2.019086), np.float64(-0.319696)], [np.float64(1.801703), np.float64(-0.084875), np.float64(-0.057986)], [np.float64(1.932341), np.float64(-0.784449), np.float64(1.234779)], [np.float64(1.083956), np.float64(-1.813056), np.float64(1.371024)], [np.float64(3.148927), np.float64(-1.324479), np.float64(1.423756)], [np.float64(1.734599), np.float64(-0.012754), np.float64(2.305949)], [np.float64(-0.12227), np.float64(0.437679), np.float64(0.100961)], [np.float64(1.685352), np.float64(-0.755655), np.float64(-1.115226)], [np.float64(0.672296), np.float64(2.252282), np.float64(0.376421)], [np.float64(1.05159), np.float64(3.138298), np.float64(0.488633)]]
