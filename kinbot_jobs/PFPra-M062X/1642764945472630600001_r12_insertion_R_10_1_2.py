geom = [[np.float64(1.364029), np.float64(-0.368875), np.float64(0.135884)], [np.float64(3.086666), np.float64(-0.766443), np.float64(0.474206)], [np.float64(-0.131775), np.float64(0.007917), np.float64(0.062854)], [np.float64(-0.532419), np.float64(0.373682), np.float64(1.508732)], [np.float64(-1.790093), np.float64(0.776875), np.float64(1.589833)], [np.float64(-0.434705), np.float64(-0.634048), np.float64(2.368964)], [np.float64(0.211098), np.float64(1.356228), np.float64(1.995973)], [np.float64(-0.332247), np.float64(1.001564), np.float64(-0.815913)], [np.float64(-0.901201), np.float64(-0.971393), np.float64(-0.411921)], [np.float64(2.444713), np.float64(0.741682), np.float64(-0.48676)], [np.float64(1.945437), np.float64(1.454214), np.float64(-0.90975)]]
