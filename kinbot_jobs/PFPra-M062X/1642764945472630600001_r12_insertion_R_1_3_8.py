atom = [np.str_('C'), np.str_('O'), np.str_('C'), np.str_('C'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('O'), np.str_('H')]
geom = [[np.float64(1.536349), np.float64(-0.090108), np.float64(-0.1733)], [np.float64(2.09328), np.float64(-0.824185), np.float64(-0.906476)], [np.float64(0.006882), np.float64(-0.449588), np.float64(0.392751)], [np.float64(-0.58947), np.float64(0.353789), np.float64(1.437819)], [np.float64(-1.923296), np.float64(0.267194), np.float64(1.525729)], [np.float64(-0.157594), np.float64(0.003451), np.float64(2.658492)], [np.float64(-0.341391), np.float64(1.6647), np.float64(1.346508)], [np.float64(0.384375), np.float64(1.007018), np.float64(-1.212178)], [np.float64(-0.625822), np.float64(-1.311851), np.float64(-0.228698)], [np.float64(2.012684), np.float64(0.866234), np.float64(0.604683)], [np.float64(2.533498), np.float64(1.48475), np.float64(0.066763)]]
