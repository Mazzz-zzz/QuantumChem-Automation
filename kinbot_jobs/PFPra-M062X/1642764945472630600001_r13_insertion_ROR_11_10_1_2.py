atom = [np.str_('C'), np.str_('O'), np.str_('C'), np.str_('C'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('O'), np.str_('H')]
geom = [[np.float64(1.635647), np.float64(0.972347), np.float64(-0.221808)], [np.float64(0.09853), np.float64(1.369663), np.float64(0.994502)], [np.float64(3.120395), np.float64(1.231289), np.float64(-0.558273)], [np.float64(3.942578), np.float64(0.793654), np.float64(0.673537)], [np.float64(5.239833), np.float64(0.753041), np.float64(0.41591)], [np.float64(3.801185), np.float64(1.616654), np.float64(1.706851)], [np.float64(3.620249), np.float64(-0.414783), np.float64(1.111041)], [np.float64(3.498803), np.float64(0.579826), np.float64(-1.668301)], [np.float64(3.315245), np.float64(2.514882), np.float64(-0.859769)], [np.float64(0.627499), np.float64(0.118885), np.float64(-0.819921)], [np.float64(-0.230529), np.float64(0.513139), np.float64(0.073585)]]
