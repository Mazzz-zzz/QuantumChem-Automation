db = connect('/home/akhalilov/GaussianJobCreator/AUTOMATION-CENTER/kinbot_jobs/PFPra-M062X/kinbot.db')
label = 'conf/1642764945472630600001_r13_insertion_CO2_3_11_0000'
logfile = 'conf/1642764945472630600001_r13_insertion_CO2_3_11_0000.log'

mol = Atoms(symbols=[np.str_('C'), np.str_('O'), np.str_('C'), np.str_('C'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('O'), np.str_('H')], positions=[[np.float64(2.116723), np.float64(-0.072318), np.float64(-0.021883)], [np.float64(2.297005), np.float64(-1.182333), np.float64(-0.213877)], [np.float64(-0.049662), np.float64(0.322962), np.float64(0.073699)], [np.float64(-0.620919), np.float64(0.107562), np.float64(1.471877)], [np.float64(-1.929489), np.float64(-0.105353), np.float64(1.472208)], [np.float64(-0.022539), np.float64(-0.941359), np.float64(2.03945)], [np.float64(-0.368886), np.float64(1.183451), np.float64(2.222265)], [np.float64(-0.698216), np.float64(1.396443), np.float64(-0.466119)], [np.float64(-0.375975), np.float64(-0.753054), np.float64(-0.672044)], [np.float64(2.43893), np.float64(1.112963), np.float64(0.16544)], [np.float64(1.283897), np.float64(1.174042), np.float64(0.201487)]])

kwargs = {'method': 'M062X', 'basis': 'Def2TZVPP', 'nprocshared': 8, 'mem': '28GB', 'label': 'conf/1642764945472630600001_r13_insertion_CO2_3_11_0000', 'Symm': 'None', 'mult': 1, 'charge': 0, 'scf': 'xqc', 'pop': 'None', 'opt': 'NoFreeze,TS,CalcAll,NoEigentest,MaxCycle=999'}
