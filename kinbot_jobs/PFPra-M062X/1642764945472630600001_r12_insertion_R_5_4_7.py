geom = [[np.float64(1.471212), np.float64(0.124352), np.float64(0.131916)], [np.float64(2.323471), np.float64(-0.551367), np.float64(0.603333)], [np.float64(-0.024764), np.float64(-0.318507), np.float64(-0.063763)], [np.float64(-0.665636), np.float64(-0.108598), np.float64(1.142386)], [np.float64(-1.138084), np.float64(1.467907), np.float64(1.425801)], [np.float64(-1.122824), np.float64(-0.767282), np.float64(2.005488)], [np.float64(0.558289), np.float64(1.330027), np.float64(2.270376)], [np.float64(-0.585012), np.float64(0.347198), np.float64(-1.108988)], [np.float64(-0.06221), np.float64(-1.598365), np.float64(-0.507516)], [np.float64(1.656881), np.float64(1.351805), np.float64(-0.335323)], [np.float64(2.518174), np.float64(1.694235), np.float64(-0.051608)]]
