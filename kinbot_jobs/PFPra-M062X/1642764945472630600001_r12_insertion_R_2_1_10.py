atom = [np.str_('C'), np.str_('O'), np.str_('C'), np.str_('C'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('O'), np.str_('H')]
geom = [[np.float64(1.325959), np.float64(-0.161516), np.float64(0.059088)], [np.float64(2.789844), np.float64(-0.605483), np.float64(0.729039)], [np.float64(-0.19696), np.float64(0.091061), np.float64(0.099866)], [np.float64(-0.586014), np.float64(0.310541), np.float64(1.578147)], [np.float64(-1.890495), np.float64(0.484129), np.float64(1.716309)], [np.float64(-0.265496), np.float64(-0.71419), np.float64(2.360621)], [np.float64(-0.017552), np.float64(1.381018), np.float64(2.113447)], [np.float64(-0.525129), np.float64(1.1536), np.float64(-0.650518)], [np.float64(-0.863518), np.float64(-0.91815), np.float64(-0.460198)], [np.float64(2.741016), np.float64(0.677126), np.float64(-0.671864)], [np.float64(2.417836), np.float64(1.273272), np.float64(-1.361836)]]
