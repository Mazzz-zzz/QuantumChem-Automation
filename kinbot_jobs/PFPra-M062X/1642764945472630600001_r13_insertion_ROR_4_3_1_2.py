atom = [np.str_('C'), np.str_('O'), np.str_('C'), np.str_('C'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('F'), np.str_('O'), np.str_('H')]
geom = [[np.float64(2.446616), np.float64(1.078424), np.float64(-0.002548)], [np.float64(0.879398), np.float64(1.905568), np.float64(0.924627)], [np.float64(1.88085), np.float64(-0.25554), np.float64(0.051547)], [np.float64(-0.013894), np.float64(0.381461), np.float64(-0.012997)], [np.float64(-0.385228), np.float64(-0.648255), np.float64(-0.61151)], [np.float64(-0.529549), np.float64(0.349955), np.float64(1.115058)], [np.float64(-0.235771), np.float64(1.329266), np.float64(-0.775456)], [np.float64(2.110968), np.float64(-1.098519), np.float64(-0.884655)], [np.float64(1.940023), np.float64(-0.880875), np.float64(1.155629)], [np.float64(3.185846), np.float64(1.7281), np.float64(-0.859577)], [np.float64(3.049166), np.float64(2.680856), np.float64(-0.714142)]]
