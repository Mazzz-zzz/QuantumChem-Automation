geom = [[np.float64(1.389646), np.float64(-0.100047), np.float64(0.02161)], [np.float64(1.930621), np.float64(-1.150958), np.float64(-0.068047)], [np.float64(-0.13674), np.float64(0.175145), np.float64(-0.251767)], [np.float64(-0.678744), np.float64(0.69964), np.float64(0.911083)], [np.float64(-1.062674), np.float64(-0.434155), np.float64(2.075558)], [np.float64(0.686421), np.float64(0.141713), np.float64(2.543579)], [np.float64(-1.088901), np.float64(1.73031), np.float64(1.280833)], [np.float64(-0.270879), np.float64(0.979374), np.float64(-1.34688)], [np.float64(-0.732381), np.float64(-0.964335), np.float64(-0.675655)], [np.float64(2.004058), np.float64(1.034975), np.float64(0.340091)], [np.float64(2.889072), np.float64(0.859747), np.float64(0.681697)]]
